package services;

public class Repper {
	private int repperId;
	private String repperName;
	private int repperAge;
	public int getRepperId() {
		return repperId;
	}
	public void setRepperId(int repperId) {
		this.repperId = repperId;
	}
	public String getRepperName() {
		return repperName;
	}
	public void setRepperName(String repperName) {
		this.repperName = repperName;
	}
	public int getRepperAge() {
		return repperAge;
	}
	public void setRepperAge(int repperAge) {
		this.repperAge = repperAge;
	}
	
}
